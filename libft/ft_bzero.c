#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*sv;

	i = 0;
	sv = (unsigned char *)s;
	while (i < n)
	{
		sv[i] = '\0';
		i++;
	}
	s = sv;
}
